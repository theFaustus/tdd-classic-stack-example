package com.evil.inc.tdd;

public class Calculator {
    public int add(int a, int b){
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }


    public int square(int a) {
        return a * a;
    }

    public int trio(int a) {
        return a * a * a;
    }
}
